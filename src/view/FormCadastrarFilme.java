    private void configurarFormulario(){
        txtID.setEnabled(false);
        preencherCombo();
    }
    
    private void estado(boolean state){
        btnEditar.setEnabled(!state);
        btnDeletar.setEnabled(!state);
        cbxClassificacao.setEnabled(state);
        txtNome.setEnabled(state);
        txtValor.setEnabled(state);
        txtNome.setEnabled(state);
        txtValor.setEnabled(state);
        
    }
    
    private void reseta(){
        txtID.setText("");
        txtNome.setText("");
        txtValor.setText("0.0");
        cbxClassificacao.setSelectedIndex(0);
    }
    
    private void preencherCombo(){
        DefaultComboBoxModel combo = new DefaultComboBoxModel();
        combo.addElement("Livre");
        combo.addElement("10 anos");
        combo.addElement("12 anos");
        combo.addElement("14 anos");
        combo.addElement("16 anos");
        combo.addElement("18 anos");
        cbxClassificacao.setModel(combo);
    }
