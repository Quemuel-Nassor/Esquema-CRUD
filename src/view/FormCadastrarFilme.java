    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        Filme deletar = new Filme(Integer.parseInt(txtID.getText()),txtNome.getText(),Double.parseDouble(txtValor.getText()),cbxClassificacao.getSelectedItem().toString());
        JOptionPane.showMessageDialog(null,"Cadastro "+new FilmeDAO().remover(deletar)+" deletado com sucesso!");
        reseta();
        btnEditar.setEnabled(false);
        btnDeletar.setEnabled(false);
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Filme novo = new Filme();
        novo.setNome(txtNome.getText());
        novo.setValor(Double.parseDouble(txtValor.getText()));
        novo.setClassificacao(cbxClassificacao.getSelectedItem().toString());
        
        int id = -1;
        if(txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Cadastro"+new FilmeDAO().inserir(novo)+" inserido com sucesso!");
        }else{
            novo.setId(Integer.parseInt(txtID.getText()));
            JOptionPane.showMessageDialog(null,"Cadastro"+new FilmeDAO().atualizar(novo)+" atualizado com sucesso!");
        }
        btnDeletar.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

