    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Double total = 0.0;
        Locacao novo = new Locacao();
        novo.setCliente(txtCliente.getText());
        novo.setData(txtData.getText());
        novo.setValor_total(Double.parseDouble(txtValor_total.getText()));
        novo.setFilme1(cbxFilmes1.getSelectedItem().toString());
        novo.setFilme2(cbxFilmes2.getSelectedItem().toString());
        novo.setFilme3(cbxFilmes3.getSelectedItem().toString());    
        
        System.out.println("linha 294, "+ novo.getFilme1()
+novo.getFilme2()
+novo.getFilme3());
        
        if(txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Cadastro"+new LocacaoDAO().inserir(novo)+" inserido com sucesso!");
        }else{
            novo.setId(Integer.parseInt(txtID.getText()));
            JOptionPane.showMessageDialog(null,"Cadastro"+new LocacaoDAO().atualizar(novo)+" atualizado com sucesso!");
        }
        for(Filme l : filmes){
            if(l.getNome().equals(cbxFilmes1.getSelectedItem())){
                total += l.getValor();
            }
        
            if(l.getNome().equals(cbxFilmes3.getSelectedItem())){
                total += l.getValor();
            }
        
            if(l.getNome().equals(cbxFilmes3.getSelectedItem())){
                total += l.getValor();
            }
        }
        txtValor_total.setText(String.valueOf(total));
        btnDeletar.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

