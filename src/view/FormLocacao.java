    private void configurarFormulario(){
        txtID.setEnabled(false);
        txtValor_total.setEnabled(false);
        filmes = new FilmeDAO().listar();
        preencherComboFilmes();
    }
    
    private void estado(boolean state){
        btnEditar.setEnabled(!state);
        btnDeletar.setEnabled(!state);
        txtCliente.setEnabled(state);
        txtData.setEnabled(state);
        cbxFilmes1.setEnabled(state);
        cbxFilmes2.setEnabled(state);
        cbxFilmes3.setEnabled(state);
    }
    
    private void reseta(){
        txtID.setText("");
        txtCliente.setText("");
        txtData.setText("");
        txtValor_total.setText("0.0");
        cbxFilmes1.setSelectedIndex(0);
        cbxFilmes2.setSelectedIndex(0);
        cbxFilmes3.setSelectedIndex(0);
    }
    
    private void preencherComboFilmes(){
        
        DefaultComboBoxModel combo1 = new DefaultComboBoxModel();
        DefaultComboBoxModel combo2 = new DefaultComboBoxModel();
        DefaultComboBoxModel combo3 = new DefaultComboBoxModel();
        
        for(Filme f : filmes){
            combo1.addElement(f.getNome());
            combo2.addElement(f.getNome());
            combo3.addElement(f.getNome());
        }
        cbxFilmes1.setModel(combo1);
        cbxFilmes2.setModel(combo2);
        cbxFilmes3.setModel(combo3);
    }
